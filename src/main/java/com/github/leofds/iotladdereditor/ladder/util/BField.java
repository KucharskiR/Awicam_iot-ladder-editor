package com.github.leofds.iotladdereditor.ladder.util;

public class BField {

	private int x1;
	private int x2;
	private int y1;
	private int y2;
	
	public BField(int x1, int x2, int y1, int y2) {
		this.x1 = x1;
		this.x2 = x2;
		this.y1 = y1;
		this.y2 = y2;
	}
	
	public boolean contain(int x,int y){
		if(x >= x1 && x <= x2){
			if(y >= y1 && y <= y2){
				return true;
			}
		}
		return false;
	}
}
